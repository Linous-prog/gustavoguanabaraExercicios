from math import floor, trunc
v1 = float(input('Digite um valor: '))
print(f'O valor digitado foi {v1} e a sua porção inteira é {floor(v1)}.')


###

v2 = float(input('Digite outro valor: '))
print(f'O valor digitado foi {v2} e a sua porção inteira é {trunc(v2)}.')


##

v3 = float(input('Digite outro valor: '))
print(f'O valor digitado foi {v3} e a sua porção inteira é {v3:.0f}.')




v4 = float(input('Digite outro valor: '))
print(f'O valor digitado foi {v4} e sua porção inteira é {int(v4)}.')



#### tudo serve para a mesma coisa, mas são formas diferentes de fazer.